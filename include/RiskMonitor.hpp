#pragma once
// =============================================================================
// RiskMonitor.hpp -- per-engine logging-only risk surveillance
// =============================================================================
//
// 2026-05-08 (Claude / Jo S20+): Single component that watches every engine's
//   closed trades and per-hour fire rate, evaluates three "would have tripped"
//   conditions, and emits log lines. v1 is logging-only -- it never touches
//   any engine's shadow_mode flag. After 2 weeks of clean log data confirming
//   the thresholds are sensible, v2 will replace the WOULD-TRIP log lines with
//   actual `engine.shadow_mode = true` writes.
//
// THE THREE CHECKS
//
//   (1) WR break-even floor.
//       Trip if rolling-N-trade win rate falls below a per-engine TRIP_WR
//       threshold (= BE_WR + buffer, calibrated by calibrate_risk_thresholds).
//       For GoldMicroScalper rk12: BE_WR=79.16%, TRIP_WR=82.16%. The math
//       guarantees that below TRIP_WR the engine is bleeding faster than the
//       3pp safety margin allows, regardless of what backtest WR was.
//
//   (2) Fire-rate over/under-firing.
//       Per-UTC-hour bucket. Trip if (a) live count for the closed hour
//       exceeds 2.5x the calibrated expected count for that hour (over-firing
//       = regime mismatch, engine seeing breakouts where backtest saw chop),
//       OR (b) live count falls below 0.4x expected for 3 consecutive active
//       hours (sustained under-firing = data feed degraded or signal model
//       fundamentally not seeing the same tape). Asymmetric thresholds
//       because under-firing is usually benign and slow; over-firing is
//       usually a fast bleed.
//
//   (3) Spread-at-entry drift.
//       The engine's MAX_SPREAD gate ensures live `spread_at_entry` <= 1.0pt
//       on XAUUSD. Backtest filtered median was 0.22 -- the broker's normal
//       spread is essentially deterministic, so any rolling drift is a
//       material signal. Trip if rolling-N-trade median > 1.5x backtest
//       filtered median (= 0.33 for microscalper). Rare wide-but-allowed
//       trades (0.40-0.99) bring the median up only when the underlying
//       broker spread regime has shifted, which is exactly what we want
//       to catch.
//
// CALIBRATION INPUT
//
//   data/risk_monitor_thresholds.csv -- generated by
//   backtest/calibrate_risk_thresholds. Schema is column-by-name; reordered
//   exports work. Add an engine to monitoring by adding a row to ENGINE_TABLE
//   in the calibration tool, recompiling, re-running, and restarting the
//   live process. No live-side code changes required.
//
// LIVE-SIDE INSERTION POINTS (the v1 wiring is FOUR places)
//
//   1. include/globals.hpp -- declare `static omega::RiskMonitor g_risk_monitor;`
//   2. include/engine_init.hpp -- call `g_risk_monitor.load_thresholds(...)`
//      at startup, after the threshold CSV path is known.
//   3. include/trade_lifecycle.hpp -- inside handle_closed_trade(), call
//      `g_risk_monitor.on_trade_close(tr_in)` near the top.
//   4. Each monitored engine's fire path -- call
//      `g_risk_monitor.on_fire("EngineName", now_s)` at the top of the order
//      send. For microscalper this lives in GoldMicroScalperEngine.hpp at the
//      [MICRO-SCALPER-GOLD] FIRE log site.
//
// LOG FORMAT
//
//   logs/risk_monitor_YYYY-MM-DD.log, append mode, day-rotated automatically.
//   One line per evaluation; OK lines emitted every Nth trade to bound noise;
//   WOULD-TRIP lines emitted on every triggering trade.
//
//   [RISK-MON] 2026-05-09 MicroScalperGold WR-EVAL: rolling_n=120 wr=84.200% trip_wr=82.160% margin=+2.04pp OK
//   [RISK-MON] 2026-05-09 MicroScalperGold FIRE-EVAL: hour=14 live=145 expected=81.6 ratio=1.78x OK
//   [RISK-MON] 2026-05-09 MicroScalperGold SPREAD-EVAL: rolling_n=120 median=0.2200 trip=0.3300 baseline_median=0.2200 OK
//   [RISK-MON] 2026-05-09 MicroScalperGold WR-EVAL: rolling_n=200 wr=78.500% trip_wr=82.160% margin=-3.66pp WOULD-TRIP
//   [RISK-MON-DAILY] 2026-05-09 MicroScalperGold fires=1247 wins=1112 losses=135 wr=89.176% pnl=985.20 trips_wr=1 trips_fire=0 trips_spread=0
//
// THREAD-SAFETY
//
//   All public methods take an internal mutex. The live tick path already
//   serialises ledger writes through handle_closed_trade(); the additional
//   per-call lock is on a different mutex but on the same code path, so
//   there is no nested-lock risk. Fire-path on_fire() is also serialised --
//   the engines that call it run on the single-threaded tick loop today,
//   but the lock keeps the contract right if that ever changes.
// =============================================================================

