// -----------------------------------------------------------------------------
// Per-engine threshold row loaded from data/risk_monitor_thresholds.csv.
// -----------------------------------------------------------------------------
struct RiskMonitorThresholds {
    std::string engine;
    std::string symbol;
    double      tp_pts                          = 0.0;
    double      sl_pts                          = 0.0;
    double      be_wr                           = 0.0;
    double      trip_wr                         = 0.0;
    double      expected_fires_per_hour         = 0.0;
    int         session_start_utc               = 0;
    int         session_end_utc                 = 0;
    double      backtest_spread_median          = 0.0;  // raw market view (diagnostic)
    double      backtest_spread_p95             = 0.0;  // raw market view (diagnostic)
    double      backtest_spread_median_filt     = 0.0;  // post-MAX_SPREAD (consumed)
    double      backtest_spread_p95_filt        = 0.0;  // post-MAX_SPREAD (diagnostic)
    int         window_n                        = 0;
    int         window_n_minimum                = 0;
    std::array<double, 24> fires_per_hour_table{};

    // Derived trip thresholds (computed at load time).
    double      spread_trip_median              = 0.0;  // rolling median > this => trip
    double      fire_under_ratio                = 0.4;  // rolling/expected < this => under
    double      fire_over_ratio                 = 2.5;  // rolling/expected > this => over
    int         fire_under_consec_hours         = 3;    // need this many in a row to trip
};

// -----------------------------------------------------------------------------
// Per-engine rolling state.
// -----------------------------------------------------------------------------
struct RiskMonitorState {
    std::string engine;
    std::deque<bool>   wins;                     // rolling N: each closed trade win/loss
    std::deque<double> spreads_at_entry;         // rolling N: spread at entry
    int64_t            last_close_ts_s          = 0;

    // Fire-rate tracking. Per-UTC-hour bucket of in-progress count.
    int                cur_hour_bucket          = -1;   // UTC hour we're aggregating
    int                cur_hour_count           = 0;    // fires this hour
    int                consec_under_hours       = 0;    // consecutive under-firing hours

    // Daily aggregates for the daily summary writer.
    int                day_fires                = 0;
    int                day_wins                 = 0;
    int                day_losses               = 0;
    double             day_pnl_sum              = 0.0;
    int                day_trip_wr              = 0;
    int                day_trip_fire            = 0;
    int                day_trip_spread          = 0;
    int                day_utc_idx              = -1;

    // Cumulative counters since process start (diagnostic only).
    int                cumulative_trip_wr       = 0;
    int                cumulative_trip_fire     = 0;
    int                cumulative_trip_spread   = 0;
};

// -----------------------------------------------------------------------------
// RiskMonitor -- owns all per-engine state and writes the log file.
// -----------------------------------------------------------------------------
class RiskMonitor {
public:
    // 2026-05-08 DEEPSTRIKE: flipped to false for the live deploy. On any
    //   trip event the monitor would now actually write back to the
    //   engine's shadow_mode flag instead of just logging WOULD-TRIP.
    //
    //   IMPORTANT: v1 has no re-arm gate. Once an engine is auto-pinned to
    //   shadow by the monitor, it stays shadow until either (a) the operator
    //   manually flips engine.shadow_mode = false in source + redeploy, or
    //   (b) v2 ships a re-arm gate that auto-restores live on engines whose
    //   paper performance recovers to backtest expectancy. For first live
    //   this one-way trip behaviour IS the safety net; do not flip back to
    //   logging_only = true without operator awareness.
    //
    //   Also see _evaluate_wr / _evaluate_fire / _evaluate_spread below --
    //   when this flag is false those methods need to actually mutate
    //   engine state, not just log. v1 implementation logs the WOULD-TRIP
    //   line AND emits a follow-up TRIPPED line announcing the auto-pin;
    //   the actual `engine.shadow_mode = true` write happens via a small
    //   handler block in each evaluator. (See AUTO_PIN_HOOKS below.)
    bool logging_only = false;

    // Load thresholds from CSV. Returns number of rows successfully loaded.
    // Pre-existing state (if any) is preserved; thresholds are upserted.
    int load_thresholds(const std::string& csv_path);

    // Hooks called from the live tick / close paths.
    void on_fire(const std::string& engine, int64_t now_s);
    void on_trade_close(const omega::TradeRecord& tr);

    // Called from a session-end heartbeat (or end-of-day signal). Emits
    // one [RISK-MON-DAILY] line per registered engine and resets day
    // aggregates. Safe to call multiple times -- empty days emit zeros.
    void emit_daily_summary(int64_t now_s);

    // 2026-05-08 DEEPSTRIKE: register an auto-pin callback per engine.
    //   When `logging_only = false` and an evaluator hits a WOULD-TRIP
    //   condition, this callback fires. Convention: the callback should
    //   set the engine's `shadow_mode = true` and print a one-line
    //   notification to stdout. The callback is responsible for its own
    //   idempotency (don't pin if already pinned).
    void register_shadow_pin_cb(const std::string& engine,
                                std::function<void(const std::string& reason)> cb);

    // 2026-05-11 S26 §2.0c: external trip path for monitors that live
    //   outside the WR / FIRE / SPREAD evaluators (currently used by the
    //   broker-disparity hard-stop at the end of handle_closed_trade in
    //   trade_lifecycle.hpp). Same semantics as the internal `_maybe_auto_pin`
    //   except that the wrapper takes the lock since outside callers don't
    //   hold mtx_. If logging_only is false and a callback is registered
    //   for `engine`, the callback fires under the monitor lock so the
    //   engine's shadow_mode flip is serialised against the rest of the
    //   evaluator state. The callback must be idempotent -- the disparity
    //   monitor uses a session-scoped one-shot guard but it is reasonable
    //   for other future callers to invoke this on every offending tick.
    void trip_engine_to_shadow(const std::string& engine,
                               const std::string& reason);

private:
    mutable std::mutex                                            mtx_;
    std::unordered_map<std::string, RiskMonitorThresholds>        thresholds_;
    std::unordered_map<std::string, RiskMonitorState>             states_;
    std::unordered_map<std::string,
                       std::function<void(const std::string&)>>   shadow_pin_cbs_;
    std::ofstream                                                 log_;
    int                                                           current_log_day_idx_ = -1;

    void _ensure_log_open  (int64_t now_s);
    void _evaluate_wr      (RiskMonitorState& s, const RiskMonitorThresholds& t, int64_t now_s);
    void _evaluate_fire    (RiskMonitorState& s, const RiskMonitorThresholds& t, int64_t now_s);
    void _evaluate_spread  (RiskMonitorState& s, const RiskMonitorThresholds& t, int64_t now_s);
    void _maybe_auto_pin   (const std::string& engine, const std::string& reason);
    void _maybe_roll_day   (RiskMonitorState& s, int64_t now_s);

    static int         _utc_hour    (int64_t now_s);
    static int         _utc_day_idx (int64_t now_s);
    static std::string _utc_date_str(int64_t now_s);
};

